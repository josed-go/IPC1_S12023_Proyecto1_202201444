    private void cmbRegionItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbRegionItemStateChanged
        txtRegion.setText(controladorR.ObtenerRegion((String)cmbRegion.getSelectedItem()));
    }//GEN-LAST:event_cmbRegionItemStateChanged

    private boolean ValidarTextField() {
        if(txtCodigo.getText().isEmpty() || txtNombre.getText().isEmpty() || cmbRegion.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Debes rellenar todos los campos.");
            return false;
        }
        return true;
    }
    
    private void DatosRegion() {
        cmbRegion.addItem("SELECCIONA UNA REGIÓN");
        for(int i=0; i < controladorR.ObtenerRegiones().size(); i++) {
            cmbRegion.addItem(controladorR.ObtenerRegiones().get(i).getCodigo());
        }
    }
    
    public void Tipo(String tipo) {
        if(tipo.equals("AGREGAR")) {
            lblTitulo.setText("AGREGAR");
            btnAccion.setText("AGREGAR");
        } else if(tipo.equals("EDITAR")) {
            lblTitulo.setText("EDITAR");
            btnAccion.setText("EDITAR");
        }
    }
    
    public void CamposEditar(String codigo) {
        txtCodigo.setText(controladorD.ValidarSeleccion(codigo).getCodigoDepartamento());
        txtNombre.setText(controladorD.ValidarSeleccion(codigo).getNombre());
        cmbRegion.setSelectedItem(controladorD.ValidarSeleccion(codigo).getCodigoRegion());
        txtRegion.setText(controladorR.ObtenerRegion((String)cmbRegion.getSelectedItem()));
        codigoE = codigo;
        nombreE = txtNombre.getText();
    }
    
