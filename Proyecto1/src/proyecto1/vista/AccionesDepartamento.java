    private void btnAccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccionActionPerformed
        if(btnAccion.getText().equals("AGREGAR")) {
            if(ValidarTextField()) {
                if(!controladorD.ValidarCodigo(txtCodigo.getText())) {
                    if(!controladorD.ValidarDepartamento(txtNombre.getText())) {
                        controladorD.AgregarDepartamento((String)cmbRegion.getSelectedItem(), controladorR.ObtenerRegion((String)cmbRegion.getSelectedItem()), txtNombre.getText(), txtCodigo.getText());
                        JOptionPane.showMessageDialog(this, "Departamento agregado.");
                        this.setVisible(false);
                        vistaD.LlenarTabla();
                        vistaD.setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Ya existe un departamento con ese nombre.");
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Ya existe un departamento con ese código.");
                }

            }
        } else if(btnAccion.getText().equals("EDITAR")) {
            if(ValidarTextField()) {
                if(!controladorD.ValidarCodigo(txtCodigo.getText()) || codigoE.equals(txtCodigo.getText())) {
                    if(!controladorD.ValidarDepartamento(txtNombre.getText()) || nombreE.equalsIgnoreCase(txtNombre.getText())) {
                        System.out.println(txtCodigo.getText()+"|"+codigoE+"|"+nombreE);
                        controladorD.EditarDepartamento(codigoE, nombreE, (String)cmbRegion.getSelectedItem(), controladorR.ObtenerRegion((String)cmbRegion.getSelectedItem()), txtNombre.getText(), txtCodigo.getText());
                        JOptionPane.showMessageDialog(this, "Kiosco editado");
                        this.setVisible(false);
                        vistaD.LlenarTabla();
                        vistaD.setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Ya existe un departamento con ese nombre.");
                    }
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Ya existe un departamento con ese código.");
                }
            }
        }
    }//GEN-LAST:event_btnAccionActionPerformed

