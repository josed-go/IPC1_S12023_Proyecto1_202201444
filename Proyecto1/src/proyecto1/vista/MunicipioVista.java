    public void LlenarTabla() {
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"NO.", "NOMBRE", "CODIGÓ DEPARTAMENTO"}, controladorM.ObtenerMunicipios().size());
        tableM.setModel(modelo);
        
        TableModel modeloDatos = tableM.getModel();
        for(int i = 0; i < controladorM.ObtenerMunicipios().size(); i++) {
            Municipio municipio = controladorM.ObtenerMunicipios().get(i);
            modeloDatos.setValueAt(i+1, i, 0);
            modeloDatos.setValueAt(municipio.getNombre(), i, 1);
            modeloDatos.setValueAt(municipio.getCodigoDepartamento(), i, 2);
        }    
    }
    
