    private void btnActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionActionPerformed
        AccionesMunicipio accionesM = new AccionesMunicipio();
        accionesM.Tipo(btnAction.getText());
        accionesM.DatosDepartamento(lblDepartamento.getText());

        this.setVisible(false);

        if(btnAction.getText().equals("AGREGAR")) {
            accionesM.setVisible(true);
        } else if(btnAction.getText().equals("EDITAR")) {
            accionesM.TextFieldEditar((String)tableM.getValueAt(tableM.getSelectedRow(), 1));
            accionesM.setVisible(true);
        }
    }//GEN-LAST:event_btnActionActionPerformed

